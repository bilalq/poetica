    <!-- Title -->
    <div class="row">
      <div class="twelve columns centered">
        <h3>Commuter Morning</h3>
      </div>
    </div>

    <!-- Author -->
    <div class="row">
      <div class="twelve columns centered">
        <p class="author">
          <?= gravatar('bilalquadri92@gmail.com', 45) ?>
          by Bilal Quadri
        </p>
      </div>
    </div>

    <!-- Content -->
    <div class="row">
      <div class="twelve columns centered">
        <pre class="poem">
Mornings, wow, what a hassle
Get up, go back to sleep, get up again, only to sleep some more,
Repeat that over and over again a few times.
Hit the bathroom, brush teeth, shower, and shave (maybe).
Get dressed, then
make breakfast, eat breakfast, clean up breakfast.
Rush out, rush back in to grab things forgotten, then rush out again,
even faster this time.
Great start, running late.
Stuck in slow, stagnant streets.
Terrible traffic takes too long to move.
Get there late, can’t find parking,
This lot’s full, keep looking.
Run out into pouring rain,
Strong winds inverting the umbrella.
Hop on the bus, hop off, then hop on another one.
Buses don’t care that people are late,
wait ten minutes before moving.
Hop off again, now run to class.
Typical tragedy, nothing special.
Everyday story of a commuter’s morning.
        </pre>
      </div>
