      <div class="twelve columns centered">
        <h3>Truth Pantoum</h3>
      </div>
    </div>

    <!-- Author -->
    <div class="row">
      <div class="twelve columns centered">
        <p class="author">
          <?= gravatar('bilalquadri92@gmail.com', 45) ?>
          by Bilal Quadri
        </p>
      </div>
    </div>

    <!-- Content -->
    <div class="row">
      <div class="twelve columns centered">
        <pre class="poem">
Truth is One, it’s absolute and final,
Yet truth seems many, diverse, and varied,
Like a river’s streaming channel,
Always changing, never steadied.

Yes, truth is many, diverse, and varied,
In the eyes of mortal men.
Always changing, never steadied,
In the histories we pen.

In the eyes of mortal men,
Pure Truth cannot be seen.
The histories which we pen,
Are merely what we glean.

Pure Truth cannot be seen,
Yet, it truly must be there.
That which we do glean,
Is but a single share.

Truly Truth must be there,
Like a river’s streaming channel.
Though all we have is a single share,
Truth is One, it’s absolute and final.
        </pre>
      </div>
